    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de Guarda este Horario? \n Total de Horas: "+calcularHoras(), "Confirmar",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (p == JOptionPane.YES_OPTION) {
                if(ValidarHoras1(jFormattedTextField1.getText(),jFormattedTextField2.getText())==false && jCheckBox1.isSelected()==true){
                    JOptionPane.showMessageDialog(null, "TURNO1 : Hora Llegada es mayor que Hora salida \n ", "NORTFARMA", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                if(ValidarHoras1(jFormattedTextField3.getText(),jFormattedTextField4.getText())==false && jCheckBox2.isSelected()==true){
                    JOptionPane.showMessageDialog(null, "TURNO2 : Hora Llegada es mayor que Hora salida \n ", "NORTFARMA", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                if(ValidarHoras1(jFormattedTextField2.getText(),jFormattedTextField3.getText())==false && CheckSeleccionados()==true){
                    JOptionPane.showMessageDialog(null, "Hora Llegada Turno 2, es menor que Hora salida Turno 1 \n ", "NORTFARMA", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                LogicaHorariosTrabajadores lh=new LogicaHorariosTrabajadores();
                String t1_ll=jFormattedTextField1.getText();
                String t1_s=jFormattedTextField2.getText();
                String t2_ll=jFormattedTextField3.getText();
                String t2_s=jFormattedTextField4.getText();

                Horario_Trabajo ht= new Horario_Trabajo();
                ht.setT1_H_Entrada(t1_ll);
                ht.setT1_H_Salida(t1_s);
                ht.setT2_H_Entrada(t2_ll);
                ht.setT2_H_Salida(t2_s);
                ht.setObservacion(jTextArea1.getText());
                lh.Modifica_Horario_Trabajo(ht, dni);
                JOptionPane.showMessageDialog(this, "El horario se ha modificado correctamente");
                this.dispose();
            }      
    }//GEN-LAST:event_jbAceptarActionPerformed
    private String calcularHoras(){
        String car1[]=jFormattedTextField1.getText().split(":");
        int a1=Integer.parseInt(car1[0]);
        int a2=Integer.parseInt(car1[1]);
        int a3=Integer.parseInt(car1[2]);

        String car2[]=jFormattedTextField2.getText().split(":");
        int b1=Integer.parseInt(car2[0]);
        int b2=Integer.parseInt(car2[1]);
        int b3=Integer.parseInt(car2[2]);
        int h=0,m=0,s=0;
        s=b3-a3;
        if(s<0){
            s=59+s;
            m=1;
            }
        m=b2-a2-m;
        if(m<0){
            m=59+m;
            h=1;
        }
        h=b1-a1-h;
        String car3[]=jFormattedTextField3.getText().split(":");
        int c1=Integer.parseInt(car3[0]);
        int c2=Integer.parseInt(car3[1]);
        int c3=Integer.parseInt(car3[2]);

        String car4[]=jFormattedTextField4.getText().split(":");
        int d1=Integer.parseInt(car4[0]);
        int d2=Integer.parseInt(car4[1]);
        int d3=Integer.parseInt(car4[2]);
        if (d1 == 0 && d2 == 0 && d3 == 0){
          if (c1 == 0 && c2 == 0 && c3 == 0){
            d1 = 0;
          }else{
              d1 = 24;
          }

        }
        int h1=0,m1=0,s1=0;
        s1=d3-c3;
        if(s1<0){
            s1=59+s1;
            m1=1;
            }
        m1=d2-c2-m1;
        if(m1<0){
            m1=59+m1;
            h1=1;
        }
        h1=d1-c1-h1;

        int h2=0,m2=0,s2=0;
        s2=s+s1;
        if(s2>=60){
            s2=s2-60;
            m2=m2+1;
            }
        m2=m2+m+m1;
        if(m2>=60){
            m2=m2-60;
            h2=h2+1;
        }
        h2=h2+h+h1;
        String t=h2+":"+m2+":"+s2;
        return t;
    }
