    private void CargarProductosPre() {
        //listaVenta_Pre.clear();
        String Interno = jTextField1.getText().trim();
        obj = new Venta(idbotica, Interno);
        listaVenta_Pre = logventa.ListaVenta_Detalle_pre(obj);
        
        for (int i = 0; i < listaVenta_Pre.size(); i++) {
            //datosDetalle_pre[0] = i + 1;
            datosDetalle_pre[0] = listaVenta_Pre.get(i).getId_Botica();
            datosDetalle_pre[1] = listaVenta_Pre.get(i).getId_Venta();           

            if (listaVenta_Pre.get(i).getPre() == 1) {
                checkPromocion.setSelected(true);
                datosDetalle_pre[2] = true;
            } else {
                checkPromocion.setSelected(false);
                datosDetalle_pre[2] = false;
            }

            if (listaVenta_Pre.get(i).getAnul() == 1) {
                checkPromocion.setSelected(true);
                datosDetalle_pre[3] = true;
            } else {
                checkPromocion.setSelected(false);
                datosDetalle_pre[3] = false;
            }

            model_pre.addRow(datosDetalle_pre);
        }
    }
