    public void run() {
        String sistema = "Windows";

        try {

            parameters.put("IDBOTICA", idbotica);
            parameters.put("INTERNO", entidadnota.getNumero_Documento());
            parameters.put("REPORT_CONNECTION", con);
            report_file = this.getClass().getResource("/Reportes/Nota_Credito.jasper");

            if (obj.getsSistemaOperativo().indexOf(sistema) != -1) {
                parameters.put("SUBREPORT_DIR", "Reportes/");
            } else {
                parameters.put("SUBREPORT_DIR", "//Reportes//");
            }

            //   report_file = System.getProperty("user.dir")+"\\src\\Reportes\\VentasxVendedor.jasper";
            masterReport = (JasperReport) JRLoader.loadObject(report_file);
            jasperPrint = JasperFillManager.fillReport(masterReport, parameters, con);
            JasperViewer view = new JasperViewer(jasperPrint, false);
            view.setTitle("NOTA DE CREDITO");
            view.setVisible(true);
            view.setSize(850, 650);

        } catch (JRException ex) {
            System.out.println(ex.getMessage());
        }

        jButton2.setEnabled(true);

    }

