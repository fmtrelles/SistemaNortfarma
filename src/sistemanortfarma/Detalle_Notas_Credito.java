    private void MuestraDetalle() {
        milista = logcreditos.Lista_Detalle_NotasCredito(idbotica, num_doc);

        for (int i = 0; i < milista.size(); i++) {
            datosDetalle[0] = i + 1;
            datosDetalle[1] = milista.get(i).getProducto();
            datosDetalle[2] = milista.get(i).getUnd();
            datosDetalle[3] = milista.get(i).getFracc();
            datosDetalle[4] = milista.get(i).getPvp();
            model.addRow(datosDetalle);
        }

    }

