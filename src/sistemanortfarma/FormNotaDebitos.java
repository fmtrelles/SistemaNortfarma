private void llenarInternos(String IdProducto){
    DefaultTableModel model2=(DefaultTableModel) jTable2.getModel();
        jTable2.setModel(model2);
        ArrayList<Venta> ventas= new ArrayList<Venta>();
        ventas=logventa.Lista_Internos_Para_Ndebito(IdProducto);

        for (int i = 0; i < ventas.size(); i++) {
            Venta temp=ventas.get(i);
            Object[] fila=new Object[8];
            fila[0]=temp.getId_Venta();
            fila[1]=temp.getFechaVta();
            fila[2]=temp.getSerie();
            fila[3]=temp.getNumero();
            fila[4]=temp.getDescr_Producto();
            fila[5]=String.valueOf(temp.getUnidad());
            fila[6]=String.valueOf(temp.getFraccion());
            fila[7]=String.valueOf(temp.getTotal_producto());
            model2.addRow(fila);
            }
        jTable2.setModel(model2);
}
