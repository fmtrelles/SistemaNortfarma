    private void ConfirmarVenta() {
        String usuario = "";
        String clave = "";

        try {



            if (this.jTextField1.getText().trim().compareTo("") != 0 && this.jPasswordField1.getText().trim().compareTo("") != 0) {
                usuario = jTextField1.getText().trim();
                clave = jPasswordField1.getText().trim();
                List<Personal> lista = new ArrayList<Personal>();
                LogicaPersonal logper = new LogicaPersonal();
                obj = new Personal();
                obj.setDNI(usuario);
                obj.setContrasena(clave);
                obj.setId_botica(idbotica);
                lista = logper.Verifica_Usuario_Cambio(obj);
                int idpersonal = lista.get(0).getId_Personal();
                Venta p = new Venta(idbotica, idpersonal);
                boolean resul = objguardaventa.Verifica_Venta_Botiquin(p);

                if (resul) {
                    dispose();
                    objcotiza.Guardar();
                } else {
                    JOptionPane.showMessageDialog(this, " Lo Sentimos no tiene Permisos Para Realizar esta Venta", "Error", JOptionPane.ERROR_MESSAGE);
                    this.jTextField1.setText("");
                    this.jPasswordField1.setText("");
                    this.jTextField1.requestFocus();
                }


            } else {
                JOptionPane.showMessageDialog(this, " Porfavor Ingrese Usuario y Clave  ", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            System.out.println("VENTA BOTIQUIN" + ex.getMessage());
        }

    }
