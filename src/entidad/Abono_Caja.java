public class Abono_Caja {

    private Boticas botica;
    private Cajas caja;
    private String FECHA;
    private Personal persona;
    private Turno turno;
    private Billetes_Monedas biilete;
    private int Cantidad;
    private double Total;

    public int getCantidad() {
        return Cantidad;
    }

    public void setCantidad(int Cantidad) {
        this.Cantidad = Cantidad;
    }

    public String getFECHA() {
        return FECHA;
    }

    public void setFECHA(String FECHA) {
        this.FECHA = FECHA;
    }

    public double getTotal() {
        return Total;
    }

    public void setTotal(double Total) {
        this.Total = Total;
    }

    public Billetes_Monedas getBiilete() {
        return biilete;
    }

    public void setBiilete(Billetes_Monedas biilete) {
        this.biilete = biilete;
    }

    public Boticas getBotica() {
        return botica;
    }

    public void setBotica(Boticas botica) {
        this.botica = botica;
    }

    public Cajas getCaja() {
        return caja;
    }

    public void setCaja(Cajas caja) {
        this.caja = caja;
    }

    public Personal getPersona() {
        return persona;
    }

    public void setPersona(Personal persona) {
        this.persona = persona;
    }

    public Turno getTurno() {
        return turno;
    }

    public void setTurno(Turno turno) {
        this.turno = turno;
    }
}
