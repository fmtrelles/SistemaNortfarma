public class Sobrantes {

    private int idSob;
    private String codSob;
    private double totalSobrante;

    public Sobrantes(){

    }

    public String getCodSob() {
        return codSob;
    }

    public void setCodSob(String codSob) {
        this.codSob = codSob;
    }

    public int getIdSob() {
        return idSob;
    }

    public void setIdSob(int idSob) {
        this.idSob = idSob;
    }

    public double getTotalSobrante() {
        return totalSobrante;
    }

    public void setTotalSobrante(double totalSobrante) {
        this.totalSobrante = totalSobrante;
    }



}
